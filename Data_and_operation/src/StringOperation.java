
public class StringOperation {

	public static void main(String[] args) {
		
		//length 글자수 명령어
		System.out.println("Hello World".length());
		
		//repace a를 b로 바꿔줘! (name,a / dongsoo,b) / 이름만바꿔서 반복할때 좋음
		System.out.println("Hello, [[[name]]] ...bye. ".replace("[[[name]]]", "DongSoo"));
		
	}

}
